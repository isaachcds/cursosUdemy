let maior = Math.max(6, 12, 28, 55, 100,8);
console.log(maior);
// Essa função denominada "Math.max" serve para identificar qual é o maior valor da função...
// A resposta dessa função é 100.

let menor = Math.min(100, 2, 1.8, 3, 40, 200);
console.log(menor);
// Essa função denominada "Math.min" serve para identificar qual é o MENOR valor da função..
// A resposta dessa função é 1.8

